

public class Triangle extends Shape {
private int base=10;
private int height=10;
private double area;

public Triangle()
{
	
}


public int getBase() {
	return base;
}
public void setBase(int base) {
	this.base = base;
}
public int getHeight() {
	return height;
}
public void setHeight(int height) {
	this.height = height;
}
public double area()
{
	return this.area=base*height;
}




}
