

public class Rectangle extends Shape{
private int width=20;
private int length=20;
private double area;
public int getWidth() {
	return width;
}
public void setWidth(int width) {
	this.width = width;
}
public int getLength() {
	return length;
}
public void setLength(int length) {
	this.length = length;
}

public double area()
{
	return this.area=length*width;
}

}
